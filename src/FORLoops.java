                       
public class FORLoops {
public static void main(String[] args) {
	for(int k = 0; k < 101; k++) {
	System.out.print(k+" ");	
	}
	System.out.println();
	
	for (int i = 100; i >= 0; i--) {
		System.out.print(i+" ");
	}
	System.out.println();
	
	for(int k= 2; k<101; k+=2){
	System.out.print(k+" ");
	}
	System.out.println();
	
	for(int b=1; b<100; b+=2){
		System.out.print(b+" ");
	}
	
}
}


